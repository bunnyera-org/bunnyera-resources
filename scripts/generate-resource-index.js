import fs from 'fs';
import path from 'path';

const rootDir = process.cwd();
const outputFile = path.join(rootDir, 'RESOURCE_INDEX.md');

const allowedExtensions = new Set([
  '.png',
  '.jpg',
  '.jpeg',
  '.webp',
  '.svg',
  '.zip'
]);

const ignoredFolders = new Set([
  '.git',
  'node_modules'
]);

function walk(dir) {
  if (!fs.existsSync(dir)) {
    return [];
  }

  const results = [];

  for (const item of fs.readdirSync(dir)) {
    const fullPath = path.join(dir, item);
    const stat = fs.statSync(fullPath);

    if (stat.isDirectory()) {
      if (ignoredFolders.has(item)) {
        continue;
      }

      results.push(...walk(fullPath));
    } else {
      results.push(fullPath);
    }
  }

  return results;
}

function groupByFolder(files) {
  const grouped = {};

  for (const file of files) {
    const relativePath = path.relative(rootDir, file);
    const folder = path.dirname(relativePath);

    if (!grouped[folder]) {
      grouped[folder] = [];
    }

    grouped[folder].push(relativePath);
  }

  return grouped;
}

function main() {
  const files = walk(rootDir).filter((filePath) => {
    return allowedExtensions.has(path.extname(filePath).toLowerCase());
  });

  const grouped = groupByFolder(files);

  const lines = [];

  lines.push('# BunnyEra Resource Index');
  lines.push('');
  lines.push('This file is automatically generated by `npm run index`.');
  lines.push('');
  lines.push(`Generated at: ${new Date().toISOString()}`);
  lines.push('');
  lines.push('## Summary');
  lines.push('');
  lines.push(`Total assets: ${files.length}`);
  lines.push('');

  if (files.length === 0) {
    lines.push('No assets found yet.');
    lines.push('');
  }

  for (const folder of Object.keys(grouped).sort()) {
    lines.push(`## ${folder}`);
    lines.push('');

    for (const item of grouped[folder].sort()) {
      lines.push(`- \`${item.replaceAll('\\', '/')}\``);
    }

    lines.push('');
  }

  fs.writeFileSync(outputFile, lines.join('\n'), 'utf-8');

  console.log(`Resource index generated: ${path.relative(rootDir, outputFile)}`);
}

main();